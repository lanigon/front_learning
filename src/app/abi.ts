import { ethers } from "ethers"

const or = Math.random()

const INFURA_ID = or>0.5?'2c0315e866694b5f8855f369ba30a4b0':"04220587af354cc0b5c0d61f9dee2ef5"
export const provider = new ethers.JsonRpcProvider(`https://sepolia.infura.io/v3/${INFURA_ID}`)

export const getSigner = async ()=> {
  if (window.ethereum) {
    try {
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      const provider = new ethers.BrowserProvider(window.ethereum)
      const signer = await provider.getSigner();
      return signer;
    } catch (error) {
      console.error('Error getting signer:', error);
    }
  } else {
    console.error('MetaMask is not installed!');
  }
}

export const getBlockNumber = async ()=> {
  return await provider.getBlockNumber();
}

export const getContract= async(address: string, abi: any) => {
  const signer = await getSigner()
  if(signer){
    return new ethers.Contract(address, abi, signer)
  }
  else{
    return null
  }
}

export const getInfura = (address: string, abi: any)=>{
  return new ethers.Contract(address, abi, provider)
}

